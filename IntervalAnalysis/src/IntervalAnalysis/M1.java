package IntervalAnalysis;

public class M1 {
	
	
	    public static void main(String[] args) {
	        int x = 42;
	        int y = 22;
	        if(x < 20) {
	            while(x < 10) {
	                x--;
	                y = 21;
	            }
	        }else {
	            while(y < 20) {
	                y++;
	            }
	        }
	        System.out.println(x +" " + y);
	    }
	}



